#!/usr/bin/env node
// Regenerate tests/pdf/pdf-inventory.ts from the real PDF folder.
// - Lists all .pdf in ~/Downloads/peerspectiv AI docs (non-recursive)
// - Filters out admin docs (names matching /^\d+\. / or containing 'completed review')
// - Sorts alphabetically
// - Writes the new tests/pdf/pdf-inventory.ts

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const PDF_DIR = '/Users/dlnagalla/Downloads/peerspectiv AI docs';
const OUT_FILE = path.join(__dirname, '..', 'tests', 'pdf', 'pdf-inventory.ts');

function isAdminDoc(name) {
  if (/^\d+\. /.test(name)) return true; // "2. Master Services Agreement.pdf"
  if (/completed review/i.test(name)) return true; // review forms
  return false;
}

const entries = fs
  .readdirSync(PDF_DIR, { withFileTypes: true })
  .filter((d) => d.isFile() && d.name.toLowerCase().endsWith('.pdf'))
  .map((d) => d.name)
  .filter((name) => !isAdminDoc(name))
  .sort((a, b) => a.localeCompare(b, 'en', { sensitivity: 'base' }));

console.log(`Found ${entries.length} medical chart PDFs`);

const header = `// Auto-generated inventory of real medical chart PDFs from ~/Downloads/peerspectiv AI docs
// Regenerated by scripts/regenerate-pdf-inventory.mjs
// Total: ${entries.length} files, all medical charts grouped by specialty

const BASE = '/Users/dlnagalla/Downloads/peerspectiv AI docs';

export const pdfInventory = {
  medicalCharts: [
${entries.map((n) => `    \`\${BASE}/${n}\`,`).join('\n')}
  ],
  reviewForms: [],
  contracts: [],
  reports: [],
  rosters: [],
};

// Map filename prefix to specialty for AI analysis context.
// Covers the expanded reviewer roster (BH, Cardiology, HIV, Mental Health,
// OBGYN, numbered OB/GYN chart IDs, Dental, Family, etc.).
export function getSpecialtyForFile(filePath: string): string {
  const raw = filePath.split('/').pop() ?? '';
  const name = raw.toLowerCase();

  // Numbered chart IDs: "100013001_OB_Doughtie.pdf", "100082326_GYN_Doughtie.pdf"
  if (/^\\d+_ob_/i.test(raw)) return 'OB/GYN';
  if (/^\\d+_gyn_/i.test(raw)) return 'OB/GYN';

  // BH / Mental Health / Counselor => Behavioral Health
  if (name.startsWith('bh ')) return 'Behavioral Health';
  if (name.startsWith('mental health')) return 'Behavioral Health';
  if (name.startsWith('counselor')) return 'Behavioral Health';

  // Cardiology
  if (name.startsWith('cardiology')) return 'Cardiology';

  // HIV
  if (name.startsWith('hiv ')) return 'HIV';

  // OB/GYN family
  if (name.startsWith('obgyn')) return 'OB/GYN';
  if (name.startsWith('gyn')) return 'OB/GYN';
  if (name.startsWith('ob ') || name.startsWith('ob.')) return 'OB/GYN';

  // Dental (incl. lowercase "dental.pdf" / "dental 2.pdf")
  if (name.startsWith('dental')) return 'Dental';

  // Pediatrics
  if (name.startsWith('pediatrics')) return 'Pediatrics';

  // Family Medicine
  if (name.startsWith('family')) return 'Family Medicine';

  // Fallback for loose names like GILMORE / MONTREUIL
  return 'Family Medicine';
}
`;

fs.writeFileSync(OUT_FILE, header, 'utf8');
console.log(`Wrote ${OUT_FILE}`);

// Print specialty breakdown (dry import emulation)
const SP = (raw) => {
  const name = raw.toLowerCase();
  if (/^\d+_ob_/i.test(raw)) return 'OB/GYN';
  if (/^\d+_gyn_/i.test(raw)) return 'OB/GYN';
  if (name.startsWith('bh ')) return 'Behavioral Health';
  if (name.startsWith('mental health')) return 'Behavioral Health';
  if (name.startsWith('counselor')) return 'Behavioral Health';
  if (name.startsWith('cardiology')) return 'Cardiology';
  if (name.startsWith('hiv ')) return 'HIV';
  if (name.startsWith('obgyn')) return 'OB/GYN';
  if (name.startsWith('gyn')) return 'OB/GYN';
  if (name.startsWith('ob ') || name.startsWith('ob.')) return 'OB/GYN';
  if (name.startsWith('dental')) return 'Dental';
  if (name.startsWith('pediatrics')) return 'Pediatrics';
  if (name.startsWith('family')) return 'Family Medicine';
  return 'Family Medicine';
};
const counts = {};
for (const n of entries) counts[SP(n)] = (counts[SP(n)] ?? 0) + 1;
console.log('\nSpecialty breakdown:');
for (const [k, v] of Object.entries(counts).sort((a, b) => b[1] - a[1])) {
  console.log(`  ${k.padEnd(22)} ${v}`);
}
